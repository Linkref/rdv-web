/**
 * Classe qui définit un patient
 * @author paulj
 *
 */
public class Patient {
	
	/**
	 * Nom du patient
	 */
	private String nom;
	/**
	 * Prenom du patient
	 */
	private String prenom;
	
	/**
	 * 
	 * @param nom
	 * @param prenom
	 */
	public Patient(String nom, String prenom) {
		this.nom = nom;
		this.prenom = prenom;
	}

}
